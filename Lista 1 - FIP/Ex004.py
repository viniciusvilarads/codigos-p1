base = float(input("Informe o valor da base do retângulo: "))
altura = float(input("Informe o valor da altura do retângulo: "))
area = base * altura

print("Base: {}m".format(base))
print("Altura: {}m".format(altura))
print("Área: {}m2".format(area))
